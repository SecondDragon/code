export default {
    //密码
    pwd: /\w{6,9}/,
    //手机号
    mobile: /^[0-9]{11}$/,
    //中文名字
    chineseName: /^[\u4E00-\u9FA5\uf900-\ufa2d·s]{2,}$/,
    idCard: /(^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$)/,//身份证号
    //整数
    integer: /(^[0-9]*$)|(^-[0-9]+$)/,
    //邮箱
    email: /(^$|^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$)/,
    //分数
    decimal: /^[-]?\d*(\.\d+)?$/,
    //英文和数字
    alphaNum: /^[a-zA-Z0-9]*$/,
    //英文字母
    alpha: /^[a-zA-Z]*$/,
    //数字
    numeric: /^[0-9]*$/g,
    //验证码
    msgcode: /\d{4}/g,
    //url
    url: /^(?:(?:https?|ftp|http):\/\/)(?:\S+(?::\S*)?@)?(?:(?!(?:10|127)(?:\.\d{1,3}){3})(?!(?:169\.254|192\.168)(?:\.\d{1,3}){2})(?!172\.(?:1[6-9]|2\d|3[0-1])(?:\.\d{1,3}){2})(?:[1-9]\d?|1\d\d|2[01]\d|22[0-3])(?:\.(?:1?\d{1,2}|2[0-4]\d|25[0-5])){2}(?:\.(?:[1-9]\d?|1\d\d|2[0-4]\d|25[0-4]))|(?:(?:[a-z\u00a1-\uffff0-9]-*)*[a-z\u00a1-\uffff0-9]+)(?:\.(?:[a-z\u00a1-\uffff0-9]-*)*[a-z\u00a1-\uffff0-9]+)*(?:\.(?:[a-z\u00a1-\uffff]{2,})))(?::\d{2,5})?(?:[/?#]\S*)?$/i,
    //输入数字
    isNumber: /[^\d]/g,
    isFloat: /[^\d | \.| -]/g,
    //只能输入英文
    isAlpha: /[^a-zA-Z]/g,

}
export var verifyFn = function (reg, value) {
    return reg.test(value)
}